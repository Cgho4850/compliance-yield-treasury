# ─── Tool 1: Check Eligibility ───────────────────────────────────────────────

@mcp.tool()
def check_eligibility(agent_address: str, erc8004_agent_id: int) -> dict:
    """
    Check if an AI agent is eligible to stake in the compliance treasury.
    
    Verifies:
    1. ERC-3643 compliance status (KYC/identity verification)
    2. ERC-8004 agent identity ownership
    
    Args:
        agent_address: The agent's Ethereum address
        erc8004_agent_id: The agent's ERC-8004 identity token ID
    
    Returns:
        Eligibility status with reasons
    """
    try:
        treasury = get_treasury_contract()
        agent_addr = format_address(agent_address)
        
        erc3643_ok, erc8004_ok, eligible = treasury.functions.isEligible(
            agent_addr, erc8004_agent_id
        ).call()
        
        result = {
            "agent_address": agent_addr,
            "erc8004_agent_id": erc8004_agent_id,
            "erc3643_compliant": erc3643_ok,
            "erc8004_identity_valid": erc8004_ok,
            "eligible_to_stake": eligible,
            "explanation": []
        }
        
        if not erc3643_ok:
            result["explanation"].append("Agent not verified in ERC-3643 identity registry. Needs KYC/compliance verification.")
        if not erc8004_ok:
            result["explanation"].append(f"Agent does not own ERC-8004 token ID {erc8004_agent_id}. Register at ERC-8004 registry first.")
        if eligible:
            result["explanation"].append("✅ Agent is fully compliant and eligible to stake.")
            
        return result
    except Exception as e:
        return {"error": str(e), "eligible_to_stake": False}

# ─── Tool 2: Get Position ─────────────────────────────────────────────────────

@mcp.tool()
def get_position(agent_address: str) -> dict:
    """
    Get the full staking position for an agent.
    
    Shows: principal locked, current Aave balance, accrued yield,
    spending wallet, ERC-8004 agent ID, and total yield harvested.
    
    Args:
        agent_address: The agent's Ethereum address
    
    Returns:
        Complete position details with yield estimates in human-readable format
    """
    try:
        treasury = get_treasury_contract()
        agent_addr = format_address(agent_address)
        
        (
            principal_wsteth,
            current_awsteth,
            estimated_yield,
            spending_wallet,
            erc8004_id,
            total_yield_harvested,
            staked_at,
            active
        ) = treasury.functions.getPosition(agent_addr).call()
        
        # Get current wstETH/stETH exchange rate for context
        wsteth = w3.eth.contract(address=format_address(WSTETH_ADDRESS), abi=ERC20_ABI)
        
        result = {
            "agent_address": agent_addr,
            "active": active,
            "principal_locked": {
                "wei": principal_wsteth,
                "wstETH": f"{wei_to_eth(principal_wsteth):.6f} wstETH",
                "note": "Structurally locked — cannot be directly withdrawn by agent"
            },
            "current_aave_balance": {
                "wei": current_awsteth,
                "aWSTETH": f"{wei_to_eth(current_awsteth):.6f} aWSTETH"
            },
            "accrued_yield": {
                "wei": estimated_yield,
                "wstETH": f"{wei_to_eth(estimated_yield):.8f} wstETH",
                "available_to_harvest": estimated_yield > 0
            },
            "spending_wallet": spending_wallet,
            "erc8004_agent_id": erc8004_id,
            "total_yield_harvested": {
                "wei": total_yield_harvested,
                "wstETH": f"{wei_to_eth(total_yield_harvested):.8f} wstETH"
            },
            "staked_at": staked_at,
            "staked_at_human": time.strftime('%Y-%m-%d %H:%M UTC', time.gmtime(staked_at)) if staked_at > 0 else "Not staked"
        }
        
        return result
    except Exception as e:
        return {"error": str(e)}

# ─── Tool 3: Stake wstETH ─────────────────────────────────────────────────────

@mcp.tool()
def stake_wsteth(
    amount_wsteth: float,
    erc8004_agent_id: int,
    spending_wallet: Optional[str] = None
) -> dict:
    """
    Stake wstETH into the compliance-gated yield treasury.
    
    The principal is locked in Aave v3 on Base — earning yield continuously.
    Only the accrued yield flows to the spending wallet (default: Locus wallet).
    
    Prerequisites:
    - Agent must be ERC-3643 verified
    - Agent must own the specified ERC-8004 identity token
    - Agent must have approved the treasury contract to spend their wstETH
    
    Args:
        amount_wsteth: Amount of wstETH to stake (e.g., 0.1 for 0.1 wstETH)
        erc8004_agent_id: The agent's ERC-8004 identity token ID
        spending_wallet: Optional. Where yield will be sent. Defaults to Locus wallet.
    
    Returns:
        Transaction hash and position details
    """
    try:
        account = get_account()
        treasury = get_treasury_contract()
        
        # Convert to wei
        amount_wei = int(amount_wsteth * 10**18)
        target_wallet = format_address(spending_wallet or LOCUS_WALLET)
        
        # Step 1: Approve treasury to spend wstETH
        wsteth = w3.eth.contract(address=format_address(WSTETH_ADDRESS), abi=ERC20_ABI)
        
        approve_tx = wsteth.functions.approve(
            format_address(TREASURY_ADDRESS), amount_wei
        ).build_transaction({
            "chainId": 8453,  # Base mainnet
        })
        approve_result = send_transaction(approve_tx)
        
        if approve_result["status"] != "success":
            return {"error": "Approval transaction failed", "details": approve_result}
        
        # Step 2: Stake
        stake_tx = treasury.functions.stake(
            amount_wei,
            erc8004_agent_id,
            target_wallet
        ).build_transaction({
            "chainId": 8453,
        })
        stake_result = send_transaction(stake_tx)
        
        return {
            "status": "staked" if stake_result["status"] == "success" else "failed",
            "amount_staked": f"{amount_wsteth} wstETH",
            "spending_wallet": target_wallet,
            "erc8004_agent_id": erc8004_agent_id,
            "approve_tx": approve_result["basescan_url"],
            "stake_tx": stake_result["basescan_url"],
            "tx_hash": stake_result["tx_hash"],
            "note": "Principal is now locked. Yield will accrue to your spending wallet."
        }
    except Exception as e:
        return {"error": str(e)}

# ─── Tool 4: Harvest Yield ────────────────────────────────────────────────────

@mcp.tool()
def harvest_yield() -> dict:
    """
    Harvest accrued yield and send it to the agent's spending wallet.
    
    Only the yield (above the locked principal) is withdrawn.
    The principal remains in Aave, continuing to earn interest.
    
    Returns:
        Amount harvested and transaction details
    """
    try:
        treasury = get_treasury_contract()
        account = get_account()
        
        # Check current position first
        position = get_position(account.address)
        if "error" in position:
            return position
        
        if not position["active"]:
            return {"error": "No active staking position. Stake first."}
        
        yield_wei = position["accrued_yield"]["wei"]
        if yield_wei == 0:
            return {
                "status": "no_yield",
                "message": "No yield available to harvest yet. Position may be too new.",
                "staked_at": position["staked_at_human"]
            }
        
        # Execute harvest
        harvest_tx = treasury.functions.harvestYield().build_transaction({
            "chainId": 8453,
        })
        result = send_transaction(harvest_tx)
        
        return {
            "status": result["status"],
            "yield_harvested": position["accrued_yield"]["wstETH"],
            "sent_to": position["spending_wallet"],
            "tx_hash": result["tx_hash"],
            "basescan_url": result["basescan_url"],
            "principal_still_locked": position["principal_locked"]["wstETH"]
        }
    except Exception as e:
        return {"error": str(e)}

# ─── Tool 5: Register ERC-8004 Identity ──────────────────────────────────────

@mcp.tool()
def register_erc8004_agent(
    agent_name: str,
    agent_description: str,
    agent_endpoint: Optional[str] = None
) -> dict:
    """
    Register this agent in the ERC-8004 onchain Agent Identity Registry on Base.
    
    Creates a permanent onchain identity (NFT) for the agent at:
    0x8004A169FB4a3325136EB29fA0ceB6D2e539a432
    
    The agent ID returned is required for staking in the compliance treasury.
    
    Args:
        agent_name: Human-readable name for this agent
        agent_description: What this agent does
        agent_endpoint: Optional API endpoint for this agent
    
    Returns:
        The ERC-8004 agent token ID and transaction details
    """
    try:
        account = get_account()
        registry = w3.eth.contract(
            address=format_address(AGENT_REGISTRY_ADDRESS),
            abi=AGENT_REGISTRY_ABI
        )
        
        # Build ERC-8004 registration JSON
        registration = {
            "type": "https://eips.ethereum.org/EIPS/eip-8004#registration-v1",
            "name": agent_name,
            "description": agent_description,
            "services": [],
            "x402Support": True,
            "active": True,
            "supportedTrust": ["reputation", "tee-attestation"],
            "hackathon": "Synthesis 2026",
            "project": "ComplianceYieldTreasury"
        }
        
        if agent_endpoint:
            registration["services"].append({
                "name": "A2A",
                "endpoint": agent_endpoint,
                "version": "0.3.0"
            })
        
        # For production: upload to IPFS and use the CID as agentURI
        # For hackathon: use a data URI
        agent_uri = f"data:application/json;base64," + __import__('base64').b64encode(
            json.dumps(registration).encode()
        ).decode()
        
        metadata = __import__('eth_abi').encode(['string', 'string'], [agent_name, agent_description])
        
        tx = registry.functions.register(agent_uri, metadata).build_transaction({
            "chainId": 8453,
        })
        result = send_transaction(tx)
        
        # Get the agent ID from the transaction logs
        receipt = w3.eth.get_transaction_receipt(bytes.fromhex(result["tx_hash"]))
        # ERC-721 Transfer event — tokenId is the 3rd topic
        agent_id = None
        for log in receipt["logs"]:
            if len(log["topics"]) == 4:
                agent_id = int(log["topics"][3].hex(), 16)
                break
        
        return {
            "status": result["status"],
            "erc8004_agent_id": agent_id,
            "agent_name": agent_name,
            "owner": account.address,
            "registry": AGENT_REGISTRY_ADDRESS,
            "tx_hash": result["tx_hash"],
            "basescan_url": result["basescan_url"],
            "note": f"Save your agent ID: {agent_id}. You need it to stake in ComplianceYieldTreasury."
        }
    except Exception as e:
        return {"error": str(e)}

# ─── Tool 6: Treasury Stats ───────────────────────────────────────────────────

@mcp.tool()
def get_treasury_stats() -> dict:
    """
    Get overall treasury statistics — for the monitoring agent and dashboards.
    
    Returns:
        Total principal locked, total Aave balance, number of agents,
        estimated APY, and treasury health status.
    """
    try:
        treasury = get_treasury_contract()
        
        total_principal = treasury.functions.totalPrincipalLocked().call()
        total_aave = treasury.functions.totalAaveBalance().call()
        agent_count = treasury.functions.getAgentCount().call()
        
        # Calculate rough APY from Aave's current rate
        # liquidityRate from getReserveData is in ray (1e27)
        aave = w3.eth.contract(address=format_address(AAVE_POOL_ADDRESS), abi=[
            {"inputs": [{"name": "asset", "type": "address"}], "name": "getReserveData",
             "outputs": [
                 {"name": "configuration", "type": "uint256"},
                 {"name": "liquidityIndex", "type": "uint128"},
                 {"name": "currentLiquidityRate", "type": "uint128"},
                 {"name": "variableBorrowIndex", "type": "uint128"},
                 {"name": "currentVariableBorrowRate", "type": "uint128"},
                 {"name": "currentStableBorrowRate", "type": "uint128"},
                 {"name": "lastUpdateTimestamp", "type": "uint40"},
                 {"name": "id", "type": "uint16"},
                 {"name": "aTokenAddress", "type": "address"},
                 {"name": "stableDebtTokenAddress", "type": "address"},
                 {"name": "variableDebtTokenAddress", "type": "address"},
                 {"name": "interestRateStrategyAddress", "type": "address"},
                 {"name": "accruedToTreasury", "type": "uint128"},
                 {"name": "unbacked", "type": "uint128"},
                 {"name": "isolationModeTotalDebt", "type": "uint128"}
             ],
             "type": "function", "stateMutability": "view"}
        ])
        
        reserve_data = aave.functions.getReserveData(
            format_address(WSTETH_ADDRESS)
        ).call()
        
        # liquidityRate is in ray (1e27) — convert to APY %
        liquidity_rate_ray = reserve_data[2]
        apy = (liquidity_rate_ray / 1e27) * 100
        
        total_yield_accrued = max(0, total_aave - total_principal)
        
        return {
            "treasury_address": TREASURY_ADDRESS,
            "total_principal_locked": {
                "wei": total_principal,
                "wstETH": f"{wei_to_eth(total_principal):.6f} wstETH"
            },
            "total_aave_balance": {
                "wei": total_aave,
                "aWSTETH": f"{wei_to_eth(total_aave):.6f} aWSTETH"
            },
            "total_yield_accrued": {
                "wei": total_yield_accrued,
                "wstETH": f"{wei_to_eth(total_yield_accrued):.8f} wstETH"
            },
            "active_agents": agent_count,
            "current_apy": f"{apy:.4f}%",
            "yield_source": "Aave v3 on Base (wstETH lending)",
            "principal_lock": "STRUCTURAL — no agent can withdraw principal",
            "health": "🟢 Healthy" if total_aave >= total_principal else "🔴 Check required",
            "chain": "Base (chainId: 8453)",
            "block": w3.eth.block_number
        }
    except Exception as e:
        return {"error": str(e)}

