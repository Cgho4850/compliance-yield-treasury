# ─── Helper Functions ─────────────────────────────────────────────────────────

def get_treasury_contract():
    if not TREASURY_ADDRESS:
        raise ValueError("TREASURY_ADDRESS not set. Deploy the contract first.")
    return w3.eth.contract(address=Web3.to_checksum_address(TREASURY_ADDRESS), abi=TREASURY_ABI)

def get_account():
    if not PRIVATE_KEY:
        raise ValueError("PRIVATE_KEY not set in environment.")
    return Account.from_key(PRIVATE_KEY)

def wei_to_eth(wei: int, decimals: int = 18) -> float:
    return wei / (10 ** decimals)

def format_address(addr: str) -> str:
    """Format address with 0x prefix and checksum."""
    return Web3.to_checksum_address(addr)

def send_transaction(tx_data: dict) -> dict:
    """Sign and broadcast a transaction."""
    account = get_account()
    tx_data["from"] = account.address
    tx_data["nonce"] = w3.eth.get_transaction_count(account.address)
    tx_data["gasPrice"] = w3.eth.gas_price
    
    if "gas" not in tx_data:
        tx_data["gas"] = w3.eth.estimate_gas(tx_data)
    
    signed = account.sign_transaction(tx_data)
    tx_hash = w3.eth.send_raw_transaction(signed.raw_transaction)
    receipt = w3.eth.wait_for_transaction_receipt(tx_hash, timeout=120)
    
    return {
        "tx_hash": tx_hash.hex(),
        "block_number": receipt["blockNumber"],
        "gas_used": receipt["gasUsed"],
        "status": "success" if receipt["status"] == 1 else "reverted",
        "basescan_url": f"https://basescan.org/tx/0x{tx_hash.hex()}"
    }

